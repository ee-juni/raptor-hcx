    def __init__(self):
        super().__init__()
    
    def summarize(self, context, max_tokens=1024):
        system_prompt = "주어진 [글]에 대한 [요약]을 작성합니다. 간결하게 하되, 핵심 내용을 모두 포함시켜 요약합니다."
